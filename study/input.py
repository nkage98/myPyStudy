


def compare2valor():
    primeiro_valor = input('Digite um valor: ')
    segundo_valor = input('Digite outro valor: ')
    if primeiro_valor >= segundo_valor:
        return print(f'primeiro valor = {primeiro_valor} é maior ou igual ao segundo valor = {segundo_valor}')
    return print(f'segundo valor = {segundo_valor} é maior que o primeiro valor = {primeiro_valor}')

compare2valor()
